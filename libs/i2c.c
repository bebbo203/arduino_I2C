void begin()
{
	DDRB |= (1<<7) | (1<<4);
	PORTB |= (1<<7) | (1<<4);
	CLOCK_LEVEL = 1;
}

void begin(uint8_t address)
{
	//Clock IN e scl IN
	DDRB ^= (1<<7) | (1<<4);

	CLOCK_LEVEL = 1;
	ADDRESS = address;
}


